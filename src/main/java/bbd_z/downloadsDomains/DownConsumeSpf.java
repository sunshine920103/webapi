public class DownConsumeSpf {
    private String cmrmc;
    private String xmmc;
    private String ldh;
    private String fh;
    private String msrmc;
    private String zjhm;
    private String baje;
    private String fkfs;
    private String barq;

    public DownConsumeSpf() {
    }

    public DownConsumeSpf(String cmrmc, String xmmc, String ldh, String fh, String msrmc, String zjhm, String baje, String fkfs, String barq) {
        this.cmrmc = cmrmc;
        this.xmmc = xmmc;
        this.ldh = ldh;
        this.fh = fh;
        this.msrmc = msrmc;
        this.zjhm = zjhm;
        this.baje = baje;
        this.fkfs = fkfs;
        this.barq = barq;
    }

    public String getCmrmc() {
        return cmrmc;
    }

    public void setCmrmc(String cmrmc) {
        this.cmrmc = cmrmc;
    }

    public String getXmmc() {
        return xmmc;
    }

    public void setXmmc(String xmmc) {
        this.xmmc = xmmc;
    }

    public String getLdh() {
        return ldh;
    }

    public void setLdh(String ldh) {
        this.ldh = ldh;
    }

    public String getFh() {
        return fh;
    }

    public void setFh(String fh) {
        this.fh = fh;
    }

    public String getMsrmc() {
        return msrmc;
    }

    public void setMsrmc(String msrmc) {
        this.msrmc = msrmc;
    }

    public String getZjhm() {
        return zjhm;
    }

    public void setZjhm(String zjhm) {
        this.zjhm = zjhm;
    }

    public String getBaje() {
        return baje;
    }

    public void setBaje(String baje) {
        this.baje = baje;
    }

    public String getFkfs() {
        return fkfs;
    }

    public void setFkfs(String fkfs) {
        this.fkfs = fkfs;
    }

    public String getBarq() {
        return barq;
    }

    public void setBarq(String barq) {
        this.barq = barq;
    }
}
